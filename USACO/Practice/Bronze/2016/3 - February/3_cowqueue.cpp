#include <bits/stdc++.h>
using namespace std;

void setIO(string s) {
    freopen((s + ".in").c_str(), "r", stdin);
    freopen((s + ".out").c_str(), "w", stdout);
}

int main() {
    setIO("cowqueue");

    int N;
    cin >> N;

    int cows[N][2];
    for (int i=0; i<N; i++) {
        cin >> cows[N][0] >> cows[N][1];
    }

    
}